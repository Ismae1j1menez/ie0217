#include <iostream>
using namespace std; 

int main() {
    // loop for que aumenta en uno e imrime el numero en aumento en 
    // cada ciclo
    for (int i = 1; i <= 5; ++i) {
        cout << i << " "; 
    }
    // Linea de salto para que se vea correctamente en la consola
    cout << "\n"; 
    return 0; 
}